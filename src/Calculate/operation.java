package Calculate;

public class operation {
    public static int summa(int num1,int num2){
        return num1+num2;
    }
    public static int minus(int num1,int num2){
        return num1-num2;
    }
    public static int multi(int num1,int num2){
        return  num1*num2;
    }
    public static int div(int num1,int num2){
        return num1/num2;
    }
}
